#include "../header/libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*res;

	if (size > 0 && count > SIZE_MAX / size)
		return (NULL);
	res = malloc(count * size);
	if (!res)
		return (NULL);
	ft_bzero(res, count * size);
	return (res);
}
